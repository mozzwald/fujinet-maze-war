;RESTART ENTRY (NET-ONLY)
;------------------------
;
RESTART	LDA	#$40	;DISABLE DLI
	STA	NMIEN
	JSR	NET_ENDC	;STOP NETSTREAM IF ACTIVE
	LDA	#$FF
	STA	KEYIN
	LDA	#0	;TURN OFF SOUND
	STA	AUDC1
	STA	AUDC2
	STA	AUDC3
	STA	AUDC4
	STA	CDTMF5
	JMP	START
;
STIMER	LDA	#5	;SET TIMER 5
	LDY	# <40	;2/3 SEC DELAY
	LDX	# >40
	JSR	SETVBV
	LDA	#1	;SET FLAG FOR
	STA	CDTMF5	;TIMER #5
	RTS
;
;GAME START SETUP
;----------------
;
START	JSR	STIMER
	LDA	#4	;net-only: track all 4 server slots as active participants
	STA	PLYRS
	LDA	#0
	STA	ZMBYS
	LDA	#3
	STA	ACTIVE	;ACTIVE stores count-1
;
	LDX	#0	;ERASE WALKERS
	JSR	ERASMAN
	INX
	JSR	ERASMAN
;
;SETUP GAME SCREEN
;
	LDA	# <[MAZEDAT-$84]
	STA	POINTER
	LDA	# >[MAZEDAT-$84]	;SOURCE
	STA	POINTER+1
	LDA	# <[GAMESCR-$84]
	STA	SCRPTR
	LDA	# >[GAMESCR-$84]	;DEST
	STA	SCRPTR+1
	LDX	#1
	LDY	#$84
STSCRLP	LDA	(POINTER),Y	;GET A BYTE
	STA	(SCRPTR),Y	;SAVE IT ONCE
	INC	SCRPTR	;ADD 1 TO DEST
	BNE	STSCP2
	INC	SCRPTR+1
STSCP2	STA	(SCRPTR),Y	;SAVE TWICE +
	INY		;COPY THE NXT ONE
	BNE	STSCRLP
	INC	POINTER+1
	INC	SCRPTR+1
	DEX
	BPL	STSCRLP
;
;ERASE SCREEN BOTTOM
;
	LDA	# <BOTSCRN
	STA	SCRPTR
	LDA	# >BOTSCRN
	STA	SCRPTR+1
	LDY	#79
	LDA	#0
ERASBOT	STA	(SCRPTR),Y
	DEY
	BPL	ERASBOT
;
;SET SCREEN BOTTOM
;
	LDA	#0
	STA	NET_ROLE_MASK
	JSR	NET_SCORE_INIT
;
;INITIALIZE ACTIVE PARTICIPANTS
;
PUTMIN	LDX	ACTIVE	;SET ALL ACTIVE
SETALLP	LDA	#0	;TYPE = PLAYER
	STA	TYPE,X
	STA	SCRPND,X	;0 SCORE TO ADD
	STA	ACTFLAG,X	;ACTION TO NONE
	STA	SHOTDIR,X
	STA	SHOTMST,X
	STA	SOUND,X
	STA	MOVEST,X
	STA	DIR,X
	LDA	#3	;net-only move cadence ~= server 10 Hz
	STA	MOVRATE,X
	STA	MOVCLOK,X
	LDA	#5	;NEXT HIT SCORE=5
	STA	NXTSCR,X
	LDA	#1	;placeholder net-only coordinates until authoritative snapshot/respawn
	STA	LOCX,X
	TXA
	CLC
	ADC	#1
	STA	LOCY,X
	JSR	NET_CALC_LOC
	DEX		;DO NEXT
	BPL	SETALLP
	LDA	#$0F	;hide all actors until server authoritative state arrives
	STA	NET_DEAD_MASK
	STA	NET_ERASE_MASK
;
PUTINVB
	LDA	# <GAME	;PUT UP GAME SCRN
	STA	DLIST
	LDA	# >GAME
	STA	DLIST+1
	LDY	#7		;restore color shadows (PCOLR0..3, COLOR0..3)
GMCOLR	LDA	COLTBL,Y
	STA	PCOLR0,Y
	DEY
	BPL	GMCOLR
	LDA	#PFCOL0	;SET GAME PF COLORS (hardware regs)
	STA	COLPF0
	LDA	#PFCOL1
	STA	COLPF1
	LDA	#PFCOL2
	STA	COLPF2
	LDA	#PFCOL3
	STA	COLPF3
	LDA	#0	;init background to black
	STA	COLBK
	STA	COLOR4
	JSR	STGAMVB	;INSTALL GAME VBI
	JSR	NET_INIT	;SETUP NETSTREAM
	LDA	NET_ACTIVE	;net-only: require network path
	BNE	STRTCN
	JMP	RESTART
;
;MAIN PROGRAM LOOP
;-----------------
;
