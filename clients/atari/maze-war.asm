	ADC	# <SCORE-1
	STA	SCRPTR
	LDA	# >SCORE-1
	STA	SCRPTR+1
	BCC	STENDTX
	INC	SCRPTR+1
STENDTX	LDA	ENDTXT,Y	;AND SET IT
	STA	(SCRPTR),Y	;TO "WINS"
	DEY
	BPL	STENDTX
;
